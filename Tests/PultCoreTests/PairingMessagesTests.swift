import Foundation
import Testing
@testable import PultCore

@Test
func encodesPairingRequest() {
    #expect(
        PairingMessageCoder.encodeRequest(serviceName: "svc", clientName: "cli") == Data([
            0x08, 0x02, 0x10, 0xC8, 0x01, 0x52, 0x0A,
            0x0A, 0x03, 0x73, 0x76, 0x63, 0x12, 0x03, 0x63, 0x6C, 0x69
        ])
    )
}

@Test
func encodesPairingOptionWithHexEncoding() {
    #expect(
        PairingMessageCoder.encodeOption() == Data([
            0x08, 0x02, 0x10, 0xC8, 0x01, 0xA2, 0x01, 0x08,
            0x0A, 0x04, 0x08, 0x03, 0x10, 0x06, 0x18, 0x01
        ])
    )
}

@Test
func encodesPairingConfiguration() {
    #expect(
        PairingMessageCoder.encodeConfiguration() == Data([
            0x08, 0x02, 0x10, 0xC8, 0x01, 0xF2, 0x01, 0x08,
            0x0A, 0x04, 0x08, 0x03, 0x10, 0x06, 0x10, 0x01
        ])
    )
}

@Test
func encodesPairingSecret() {
    #expect(
        PairingMessageCoder.encodeSecret(Data([0xAB, 0xCD])) == Data([
            0x08, 0x02, 0x10, 0xC8, 0x01, 0xC2, 0x02, 0x04,
            0x0A, 0x02, 0xAB, 0xCD
        ])
    )
}

@Test
func decodesAcksAndStatuses() throws {
    let requestAck = try PairingMessageCoder.decode(Data([0x08, 0x02, 0x10, 0xC8, 0x01, 0x5A, 0x04, 0x0A, 0x02, 0x74, 0x76]))
    #expect(requestAck == PairingMessage(status: .ok, kind: .requestAck(serverName: "tv")))

    let configurationAck = try PairingMessageCoder.decode(Data([0x08, 0x02, 0x10, 0xC8, 0x01, 0xFA, 0x01, 0x00]))
    #expect(configurationAck == PairingMessage(status: .ok, kind: .configurationAck))

    let secretAck = try PairingMessageCoder.decode(Data([0x08, 0x02, 0x10, 0xC8, 0x01, 0xCA, 0x02, 0x04, 0x0A, 0x02, 0xAB, 0xCD]))
    #expect(secretAck == PairingMessage(status: .ok, kind: .secretAck(Data([0xAB, 0xCD]))))

    let badSecret = try PairingMessageCoder.decode(Data([0x08, 0x02, 0x10, 0x92, 0x03]))
    #expect(badSecret.status == .badSecret)
}
